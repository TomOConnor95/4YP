function [squaredErrorSum, diff2] = calculateSquaredErrorSum(img1,img2)
diff = int16(img1) - int16(img2);
diff2 = diff.*diff;
squaredErrorSumRGB = sum(sum(diff2));
% squaredErrorSum = (1/(0.21/0.33))*squaredErrorSumRGB(1)...
%                 + (1/(0.72/0.33))*squaredErrorSumRGB(2)...
%                 + (1/(0.07/0.33))*squaredErrorSumRGB(3);
squaredErrorSum = (1/(0.33/0.33))*squaredErrorSumRGB(1)...
                + (1/(0.33/0.33))*squaredErrorSumRGB(2)...
                + (1/(0.33/0.33))*squaredErrorSumRGB(3);
end